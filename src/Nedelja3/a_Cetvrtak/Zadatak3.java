package Nedelja3.a_Cetvrtak;

public class Zadatak3 {
    public static void main(String[] args) {
        //Inicijalizovati promenljive broj1 i broj2 sa proizvoljnim vrednostima, zatim ispisati rezultate njihovog zbira, proizvoda i ostatka pri deljenju.
        int broj1 = 13;
        int broj2 = 5;
        int zbir = broj1 + broj2;
        int proizvod = broj1 * broj2;
        int ostatak = broj1 % broj2;
        System.out.println("Zbir brojeva 13 i 5 je " + zbir + ".");
        System.out.println("Proizvod brojeva 13 i 5 je " + proizvod + ".");
        System.out.println("Ostatak pri deljenju brojeva 13 i 5 iznosi " + ostatak + ".");
    }
}
